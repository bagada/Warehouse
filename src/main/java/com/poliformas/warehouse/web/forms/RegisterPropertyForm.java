package com.poliformas.warehouse.web.forms;

public class RegisterPropertyForm {
	
	
	private String ID_Equipment;
	private String Code;
	private String Date_Buy;
	private String Date_Warranty;
	private String Serie;
	
	public String getID_Equipment() {
		return ID_Equipment;
	}
	public void setID_Equipment(String iD_Equipment) {
		ID_Equipment = iD_Equipment;
	}
	public String getCode() {
		return Code;
	}
	public void setCode(String code) {
		Code = code;
	}
	public String getDate_Buy() {
		return Date_Buy;
	}
	public void setDate_Buy(String date_Buy) {
		Date_Buy = date_Buy;
	}
	public String getDate_Warranty() {
		return Date_Warranty;
	}
	public void setDate_Warranty(String date_Warranty) {
		Date_Warranty = date_Warranty;
	}
	public String getSerie() {
		return Serie;
	}
	public void setSerie(String serie) {
		Serie = serie;
	}

}
